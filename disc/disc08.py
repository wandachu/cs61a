def multiply_lnks(lst_of_lnks):
    """
    >>> a = Link(2, Link(3, Link(5)))
    >>> b = Link(6, Link(4, Link(2)))
    >>> c = Link(4, Link(1, Link(0, Link(2))))
    >>> p = multiply_lnks([a, b, c])
    >>> p.first
    48
    >>> p.rest.first
    12
    >>> p.rest.rest.rest is Link.empty
    True
    """
    res = Link(0)  # Dummy head
    p = res
    while True:
        number = 1
        index = 0
        while index < len(lst_of_lnks):
            lst = lst_of_lnks[index]
            if lst is Link.empty:
                return res.rest
            number *= lst.first
            lst_of_lnks[index] = lst.rest
            index += 1
        p.rest = Link(number)
        p = p.rest
    return res.rest


